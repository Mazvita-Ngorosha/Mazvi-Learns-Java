
public class Main {

	public static void main(String[] args) {
		// expressions = operands and operators
		// operand = value, variables., numbers, quantity
		// operators = + - * / %
		
		int friends = 10;
		
		System.out.println("I have" + " " + friends + " " + "friends.");
		
		friends = friends +1; // can also use friends++
		 
		System.out.println("I met a new friend, now I have"+ " " + friends + " "+ "friends.");
		
		friends = friends - 2;
		
		System.out.println(friends);
		
		friends = friends * 2;
		
		System.out.println(friends);
		
		friends = friends / 2;
		
		System.out.println(friends);
		
		friends = friends % 3;
		
		System.out.println(friends);
				
		

	}

}