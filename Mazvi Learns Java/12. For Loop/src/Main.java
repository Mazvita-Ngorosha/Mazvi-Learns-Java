
public class Main {

	public static void main(String[] args) {
		// A for loop executes a block of code a limited amount of times
		
		System.out.println("Hide and Seek");
		for(int i = 0; i <= 10; i++) {
			System.out.println(i);
		}
		 
		System.out.println("Ready or Not, Here I Come!\n");
		
		System.out.println("COUNTDOWN TO 2022");
		for(int i = 10; i >=0; i--) {
			System.out.println(i);
		}
		System.out.println("Happy New Year!");
	} 

}